package Code7;

public class Code7 {
    public static void main( String[] args ){
        // 比较运算符

        // 常见比较运算符：
        // == ：等于
        // != ：不等于
        // > ：大于
        // < ：小于
        // >= ：大于等于
        // <= ：小于等于
        // 注意区分=和==的写法

        // 示例：
        int a = 10;
        int b = 20;
        boolean c = a >= b;
        boolean d = a <= b;
        System.out.println("a大于等于b的结果是" + c + "，a小于等于b的结果是" + d);
    }
}
